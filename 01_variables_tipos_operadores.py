# 3. Operadores 

##      Aritméticos
a = 10
b = 3
print(a + b)  # Suma → 13
print(a - b)  # Resta → 7
print(a * b)  # Multiplicación → 30
print(a / b)  # División → 3.333...
print(a % b)  # Módulo → 1
print(a ** b) # Potencia → 1000

##      Comparación
print(a == b)  # Igualdad → False
print(a != b)  # Diferente → True
print(a > b)   # Mayor → True
print(a < b)   # Menor → False


##      Lógicos
x = True
y = False
print(x and y)  # AND → False
print(x or y)   # OR → True
print(not x)    # NOT → False
